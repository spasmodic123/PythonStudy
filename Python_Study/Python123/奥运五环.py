#奥运五环
import turtle
turtle.width(10)
turtle.color("blue")
turtle.circle(50)
turtle.penup()
turtle.goto(70,0)
turtle.pendown()
turtle.color("black")
turtle.circle(50)
turtle.penup()
turtle.goto(140,0)
turtle.pendown()
turtle.color("red")
turtle.circle(50)
turtle.penup()
turtle.goto(35,-36)
turtle.color("yellow")
turtle.pendown()
turtle.circle(50)
turtle.penup()
turtle.goto(105,-36)
turtle.pendown()
turtle.color("green")
turtle.circle(50)
